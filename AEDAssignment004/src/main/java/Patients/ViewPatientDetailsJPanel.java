    private void btnDeleteVitalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteVitalsActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = vitalTable.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

//        DefaultTableModel model = (DefaultTableModel) vitalTable.getModel();
        Encounter e = (Encounter) vitalTable.getValueAt(selectedRowIndex, 1);
        encounterHistory.removeEncounter(e);
        JOptionPane.showMessageDialog(null, "Selected Row is deleted", "Info", JOptionPane.INFORMATION_MESSAGE);
        refreshTable();
    }//GEN-LAST:event_btnDeleteVitalsActionPerformed

    private void btnUpdateDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDetailsActionPerformed
        // TODO add your handling code here:
        try {

            int selectedRowIndex = vitalTable.getSelectedRow();

            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
//            DefaultTableModel model = (DefaultTableModel) vitalTable.getModel();
            Encounter e = (Encounter) vitalTable.getValueAt(selectedRowIndex, 1);

            if (e != null) {
                UpdatePatientDetailsJPanel updjp = new UpdatePatientDetailsJPanel(userProcessContainer, e ,patient);
                userProcessContainer.add("UpdatePatientDetailsJPanel", updjp);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            } else {
                JOptionPane.showMessageDialog(null, "Nothing found", "No result found matching your criteria!!", JOptionPane.WARNING_MESSAGE);
                //return;
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Nothing found", "No result found matching your criteria!!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateDetailsActionPerformed

